num1 = int(input("Welcome. Please choose a number.\n"))
num2 = int(input("Thank you. Please choose another number.\n"))

print(num1, '+', num2, '=', num1+num2)
print(num1, '-', num2, '=', num1-num2)
print(num1, '*', num2, '=', num1*num2)
print(num1, '/', num2, '=', num1/num2)
print(num1, '%', num2, '=', num1%num2)
print(num1, '//', num2, '=', num1//num2)
print(num1, '**', num2, '=', num1**num2)